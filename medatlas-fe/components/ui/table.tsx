import * as React from 'react';
import { cn } from '@/lib/utils';

const Table = React.forwardRef<
  HTMLTableElement,
  React.HTMLAttributes<HTMLTableElement> & {
    pagination?: {
      page: number;
      totalPages: number;
      setPage: (page: number) => void;
    };
  }
>(({ className, pagination, ...props }, ref) => (
  <div className="w-full">
    <div className="relative w-full overflow-auto">
      <table ref={ref} className={cn('w-full caption-bottom text-sm', className)} {...props} />
    </div>

    {pagination && pagination.totalPages > 1 && (
      <div className="flex justify-between items-center px-6 py-4 border-t border-gray-200 bg-gray-50">
        <button
          onClick={() => pagination.setPage(Math.max(1, pagination.page - 1))}
          disabled={pagination.page === 1}
          className="px-4 py-2 text-sm bg-white border border-gray-300 rounded-md hover:bg-gray-50 disabled:opacity-50 disabled:cursor-not-allowed"
        >
          Previous
        </button>
        <span className="text-sm text-gray-600">
          Page {pagination.page} of {pagination.totalPages}
        </span>
        <button
          onClick={() => pagination.setPage(Math.min(pagination.totalPages, pagination.page + 1))}
          disabled={pagination.page === pagination.totalPages}
          className="px-4 py-2 text-sm bg-white border border-gray-300 rounded-md hover:bg-gray-50 disabled:opacity-50 disabled:cursor-not-allowed"
        >
          Next
        </button>
      </div>
    )}
  </div>
));
Table.displayName = 'Table';

